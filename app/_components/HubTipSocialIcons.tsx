const hrefFor: Record<Channel, keyof HubTipEnv> = {
  wa: 'wa',
  fb: 'fb',
  tt: 'tt',
  tg: 'tg',
  line: 'line',
};

const labelKey: Record<Channel, keyof HubTipLabels> = {
  wa: 'wa',
  fb: 'fb',
  tt: 'tt',
  tg: 'tg',
  line: 'line',
};

export function HubTipSocialIcons({ tips, labels }: { tips: HubTipEnv; labels: HubTipLabels }) {
  return (
    <ul className="hub-tip-social">
      {ORDER.map((ch) => {
        const href = tips[hrefFor[ch]]?.trim();
        if (!href) return null;
        const aria = labels[labelKey[ch]];
        return (
          <li key={ch} className="hub-tip-social__item">
            <a
              href={href}
              target="_blank"
              rel="noopener noreferrer"
              className={`hub-tip-social__btn hub-tip-social__btn--${ch}`}
              aria-label={aria}
              title={aria}
            >
              {ch === 'wa' ? <IconWa /> : null}
              {ch === 'fb' ? <IconFb /> : null}
              {ch === 'tt' ? <IconTt /> : null}
              {ch === 'tg' ? <IconTg /> : null}
              {ch === 'line' ? <IconLine /> : null}
            </a>
          </li>
        );
      })}
    </ul>
  );
}
